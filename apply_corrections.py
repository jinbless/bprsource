# Apply corrections
applied = 0
for row in rows:
    key = (row['기관명'], row['시스템명'])
    if key in corrections:
        for field, new_val in corrections[key].items():
            old_val = row[field]
            if old_val != new_val:
                row[field] = new_val
                applied += 1
                print(f'[수정] {key[0]}/{key[1]}: {field} "{old_val}" → "{new_val}"')

# Write back
with open('예산사업_시스템_매핑.csv', 'w', encoding='utf-8-sig', newline='') as f:
    writer = csv.DictWriter(f, fieldnames=fieldnames)
    writer.writeheader()
    writer.writerows(rows)

print(f'\n총 {applied}건 수정 완료')

